#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	sub;
	int	odd;

	sub = 0;
	odd = 1;
	while (nb > 0)
	{
		nb -= odd;
		sub ++;
		odd += 2;
	}
	if (nb == 0)
		return (sub);
	else
		return (0);
}
