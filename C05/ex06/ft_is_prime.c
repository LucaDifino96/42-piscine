#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	i;
	int	count;

	i = nb - 1;
	count = 1;
	if (nb < 2)
		return (0);
	while (i >= 1)
	{
		if (nb % i == 0)
			count ++;
		if (count > 2)
			return (0);
		i --;
	}
	return (1);
}
